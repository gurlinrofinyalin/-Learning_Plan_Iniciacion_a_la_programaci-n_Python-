'''
Python no tiene comandos para declarar una variable.

Una variable se crea en el momento en que se le da
un nombre y se le asigna un valor.
'''
x = 15
y = "Ana"

print(x)
print(y)

'''
mostrar el resultado de sumar 1 + 2. 
Para mostrar el resultado, debemos indicarle al programa dónde 
se encuentra dicho dato en memoria 
y, para ello, hacemos uso de una variable:
'''
# Guardamos en la variable suma el resultado de 1 + 2
suma = 1 + 2
# Accedemos al resultado de 1 + 2 a través de la variable suma
print(suma)

